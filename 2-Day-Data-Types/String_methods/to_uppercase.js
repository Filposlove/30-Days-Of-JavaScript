// toUpperCase(): this method changes the string to uppercase letters.

let string = 'JavaScript'
console.log(string.toUpperCase())      // JAVASCRIPT
let firstName = 'filpos'
console.log(firstName.toUpperCase())  // FILPOS
let lastName = "siefe"
console.log(lastName.toUpperCase())    // SEIFE
let country = 'ethiopea'
console.log(country.toUpperCase())    // ETHIOPEA